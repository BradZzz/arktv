import React, { Children } from 'react';
import PropTypes from 'prop-types';
import { ResponsiveBar } from '@nivo/bar'
import { CountByRatings, PickRandom } from '../../utils/mediaUtils'

function BarChart (props) {

  const { media } = props
  const ratings = CountByRatings(media)
  const data = []
  console.log('ratings', ratings)
  for (var rating of Object.keys(ratings)) {
      data.push({
         "rating": rating,
         "movies": ratings[rating]['movie'],
         "moviesColor": `hsl(${PickRandom(1,300)}, 70%, 50%)`,
         "television": ratings[rating]['tv'],
         "televisionColor": `hsl(${PickRandom(1,300)}, 70%, 50%)`,
      })
  }

  return <ResponsiveBar
        data={data}
        keys={[ 'movies', 'television' ]}
        indexBy="rating"
        margin={{ top: 50, right: 130, bottom: 50, left: 60 }}
        padding={0.3}
        colors={{ scheme: 'nivo' }}
        defs={[
            {
                id: 'dots',
                type: 'patternDots',
                background: 'inherit',
                color: '#38bcb2',
                size: 4,
                padding: 1,
                stagger: true
            },
            {
                id: 'lines',
                type: 'patternLines',
                background: 'inherit',
                color: '#eed312',
                rotation: -45,
                lineWidth: 6,
                spacing: 10
            }
        ]}
        fill={[
            {
                match: {
                    id: 'movies'
                },
                id: 'lines'
            }
        ]}
        borderColor={{ from: 'color', modifiers: [ [ 'darker', 1.6 ] ] }}
        axisTop={null}
        axisRight={null}
        axisBottom={{
            tickSize: 5,
            tickPadding: 5,
            tickRotation: 0,
            legend: 'rating',
            legendPosition: 'middle',
            legendOffset: 32
        }}
        axisLeft={{
            tickSize: 5,
            tickPadding: 5,
            tickRotation: 0,
            legend: 'media count',
            legendPosition: 'middle',
            legendOffset: -40
        }}
        labelSkipWidth={12}
        labelSkipHeight={12}
        labelTextColor={{ from: 'color', modifiers: [ [ 'darker', 1.6 ] ] }}
        legends={[
            {
                dataFrom: 'keys',
                anchor: 'bottom-right',
                direction: 'column',
                justify: false,
                translateX: 120,
                translateY: 0,
                itemsSpacing: 2,
                itemWidth: 100,
                itemHeight: 20,
                itemDirection: 'left-to-right',
                itemOpacity: 0.85,
                symbolSize: 20,
                effects: [
                    {
                        on: 'hover',
                        style: {
                            itemOpacity: 1
                        }
                    }
                ]
            }
        ]}
        animate={true}
        motionStiffness={90}
        motionDamping={15}
    />
}

BarChart.propTypes = {

};

export default BarChart;
